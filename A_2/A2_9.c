//9. WAP to print size of an int, char, float, double type variable
#include<stdio.h>
int main()
{
    printf("Size of an int type variable: %d\n", sizeof(int));
    printf("Size of an char type variable: %d\n", sizeof(char));
    printf("Size of an float type variable: %d\n", sizeof(float));
    printf("Size of an double type variable: %d\n", sizeof(double));
    return 0;
}